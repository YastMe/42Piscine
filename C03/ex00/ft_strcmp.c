int	ft_strcmp(char *s1, char *s2)
{
	int	result;

	result = 0;
	while ((*s1 != '\0' || *s2 != '\0') && result == 0)
	{
		if (*s1 != *s2)
			result = *s1 - *s2;
		s1++;
		s2++;
	}
	return (result);
}
