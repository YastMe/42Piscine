int	ft_str_is_printable(char *str)
{
	int	res;

	res = 1;
	if (*str != '\0')
	{
		while (*str != '\0' && res == 1)
		{
			if (*str < 32 || *str > 127)
			{
				res = 0;
			}
			str++;
		}
	}
	return (res);
}
